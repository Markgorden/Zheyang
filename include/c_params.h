static inline bool c_check_ip_mask_valid(unsigned char flag)
{
	int ip, mask;
	if (get_param_int_byindex(&g_params_context, PARAM_DHCP_INDEX, flag, GROUP_ADMINSTRATOR))
		return true;
	ip = get_param_addr_byindex(&g_params_context, PARAM_IP_INDEX, flag, GROUP_ADMINSTRATOR);
	mask = get_param_addr_byindex(&g_params_context, PARAM_MASK_INDEX, flag, GROUP_ADMINSTRATOR);
	if ((!(ip & (~mask))) || ((ip & (~mask)) == (~mask)))
		return false;
	else
		return true;
}

